function getLang() {
  return localStorage.getItem('bfl-lang') || 'en';
}

function setLang(lang) {
  localStorage.setItem('bfl-lang', lang);
  applyLang(lang);
  document.documentElement.lang = lang;
  document.querySelectorAll('.lang-btn').forEach(function (btn) {
    btn.classList.toggle('active', btn.dataset.lang === lang);
    btn.setAttribute('aria-pressed', btn.dataset.lang === lang ? 'true' : 'false');
  });
}

function applyLang(lang) {
  var dict = translations[lang] || translations['en'];

  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var key = el.dataset.i18n;
    el.textContent = dict[key] !== undefined ? dict[key] : (translations['en'][key] || key);
  });

  document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
    var key = el.dataset.i18nHtml;
    el.innerHTML = dict[key] !== undefined ? dict[key] : (translations['en'][key] || key);
  });

  var titleKey = document.documentElement.dataset.i18nTitle;
  if (titleKey) {
    var titleVal = dict[titleKey] !== undefined ? dict[titleKey] : translations['en'][titleKey];
    if (titleVal) document.title = titleVal;
  }
}

document.addEventListener('DOMContentLoaded', function () {
  var lang = getLang();
  applyLang(lang);
  document.documentElement.lang = lang;

  document.querySelectorAll('.lang-btn').forEach(function (btn) {
    btn.classList.toggle('active', btn.dataset.lang === lang);
    btn.setAttribute('aria-pressed', btn.dataset.lang === lang ? 'true' : 'false');
    btn.addEventListener('click', function () {
      setLang(btn.dataset.lang);
    });
  });
});
